-- 【业务说明】
-- 本SQL用于从ODS层同步客户基本信息到DWS层
-- 目标表: dws_yidun_corp_basic_info_dd (客户基本信息表)
-- 数据粒度: 客户级别(云账号维度)
--
-- 【数据来源】
-- 1. yidun_ods.ods_yidun_corp_info_dd - 客户信息ODS层表(原始数据)
--
-- 【输出字段说明】
-- clouduserid: 云账号ID,客户唯一标识
-- corpname: 客户名称,企业/个人客户名称
-- membershipgrade: 客户等级,会员级别(V0/V1/V2等)
-- industry_flag: 实际行业,客户所属行业分类
-- owner_name: 销售负责人,负责该客户的销售人员姓名
--
-- 【依赖说明】
-- 依赖分区: ${pre_1day} (前一天分区)
-- 执行顺序: 需确保上游ODS层数据已就绪
-- ******************************************************************** --

-- ============================================================
-- Hive参数设置
-- ============================================================
-- 合并小文件: 避免产生过多小文件,提升查询性能
set hive.merge.mapredfiles = true;

-- ============================================================
-- 数据插入: 客户基本信息同步
-- ============================================================
insert OVERWRITE table yidun_dw.dws_yidun_corp_basic_info_dd
PARTITION (pt_d = '${pre_1day}')

select
    clouduserid,                     -- 云账号ID,客户唯一标识
    corpname,                        -- 客户名称,企业/个人客户名称
    membershipgrade,                 -- 客户等级,会员级别(V0/V1/V2等)
    industry_flag,                   -- 实际行业,客户所属行业分类
    owner_name                       -- 销售负责人,负责该客户的销售人员姓名

from yidun_ods.ods_yidun_corp_info_dd

-- 筛选条件: 只处理指定分区的数据
where pt_d = '${pre_1day}';
